// declare and initialize a variable as number
var x = 5;
console.log(x);

// assign the variable another number
x = 62.36;
console.log(x);

// assign the variable a boolean value
x = true;
console.log(x);

// assign the variable a String value
x = "this is text";
console.log(x);

// assign the variable another String value
x = 'some other text';
console.log(x);
